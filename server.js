const cart = [];

const orders = [];

const express = require("express");
const app = express();
const port = 3000;
const cors = require("cors");

app.use(express.json());

app.use(cors());

app.listen(port, () => {
  console.log(`Application exemple à l'écoute sur le port ${port}!`);
});

// CARDS
// routes
app.get("/cards", (req, res) => {
  res.set("Acces-Control-Allow-Origin", "*");
  res.json(cards);
});

app.get("/cards/:id", (req, res) => {
  const id = req.params.id;
  res.set("Acces-Control-Allow-Origin", "*");
  res.json(cards.find((c) => c.id === id));
});

app.put("/cards/:id/favorite", (req, res) => {
  const id = req.params.id;
  let card = cards.find((c) => c.id === id);
  card.isFavorite = !card.isFavorite;
  res.set("Acces-Control-Allow-Origin", "*");
  res.json(card);
});

// CART
// functions
const isValid = (qty, variant) => {
  if (isNaN(qty) || qty < 1 || !Number.isInteger(qty)) return false;
  if (variant !== "normal" && variant !== "foil") return false;
  return true;
};

const find = (idToFind) => {
  return cart.find(({ id }) => idToFind === id);
};

const insert = (cartItemToInsert) => {
  cart.push(cartItemToInsert);
};

const update = (CartItemToUpdate, qty) => {
  CartItemToUpdate.qty = qty;
};

const clear = () => {
  cart.splice(0, cart.length);
};

// routes
app.get("/cart", (req, res) => {
  res.set("Acces-Control-Allow-Origin", "*");
  res.json(cart);
});

app.put("/cart", (req, res) => {
  const item = req.body;
  if (!isValid(item.qty, item.variant)) return;
  const found = find(item.id);
  found ? update(found, found.qty + item.qty) : insert(item);

  res.set("Acces-Control-Allow-Origin", "*");
  res.json(cart);
});

app.put("/cart/:id", (req, res) => {
  const id = req.params.id;
  const item = req.body.item;
  const qty = req.body.qty;
  if (!isValid(qty, item.variant)) return;
  const found = find(id);
  found && update(found, qty);
  res.set("Acces-Control-Allow-Origin", "*");
  res.json(cart);
});

app.delete("/cart/:id", (req, res) => {
  const id = req.params.id;
  const cartItemToDelete = find(id);
  res.set("Acces-Control-Allow-Origin", "*");
  cart.splice(cart.indexOf(cartItemToDelete), 1);
  res.json(cart);
});

app.get("/cart/clear", (req, res) => {
  clear();
  res.set("Acces-Control-Allow-Origin", "*");
  res.json(cart);
});

